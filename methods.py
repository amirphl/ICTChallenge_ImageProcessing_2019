def get_cart_number(array):
    number = []
    for element in array:
        for c in element[1]:
            if 48 <= ord(c) <= 57:
                number.append(c)
            elif c == 'o' or c == '&':
                number.append(0)
            elif c == 'i' or c == 'l':
                number.append(1)
            elif c == '@':
                number.append(5)
            elif c == 's':
                number.append(8)
            elif c == 'b':
                number.append(6)
            elif c == 'm':
                number.append(1)
                number.append(2)
    if len(number) == 16:
        return number
    return None

